#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*set;
	size_t			i;

	i = 0;
	set = (unsigned char *)s;
	while (i < n)
	{
		set[i] = '\0';
		i++;
	}
}
